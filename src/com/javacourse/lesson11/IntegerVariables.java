package com.javacourse.lesson11;

public class IntegerVariables {
    public static void main(String[] args) {

        // Stores whole numbers from -128 to 127.
        byte age01 = 20;

        // Stores whole numbers from -32,768 to 32,767.
        short age02 = 21;

        // Stores whole numbers from -2,147,483,648 to 2,147,483,647.
        int age03 = 22;

        // Stores whole numbers from -9,223,372,036,854,775,808 to 9,223,372,036,854,775,807.
        long age04 = 23;

        System.out.println("Value of the variable age01 = " + age01);
        System.out.println("Value of the variable age02 = " + age02);
        System.out.println("Value of the variable age03 = " + age03);
        System.out.println("Value of the variable age04 = " + age04);
    }
}
