package com.javacourse.lesson11;

public class CharInJava {
    public static void main(String[] args) {

        // Stores a single character/letter or ASCII values
        char o = 111; //char o = 'o';
        char i = 105; //char i = 'i';
        char questionMark = 0x003F; // (?) in unicode.

        System.out.println("" + o + i + questionMark);
    }
}
