package com.javacourse.lesson13;

public class RelationalOperators {

    public static void main(String[] args) {
        int value1 = 1;
        int value2 = 2;

        // Is Equal To
        System.out.println("value1 == value2: " + (value1 == value2));

        // Not Equal To
        System.out.println("value1 != value2: " + (value1 != value2));

        // Greater Than
        System.out.println("value1 > value2: "  + (value1 > value2));

        // Less Than
        System.out.println("value1 < value2: "  + (value1 < value2));

        // Greater Than or Equal To
        System.out.println("value1 >= value2: " + (value1 >= value2));

        // Greater Than or Equal To
        System.out.println("value1 <= value2: " + (value1 <= value2));
    }
}
